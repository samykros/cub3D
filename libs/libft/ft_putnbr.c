#include "libft.h"

void	ft_putnbr(int x)
{
	if (x == -2147483648)
		ft_putstr("-2147483648");
	if (x < 0)
	{
		ft_putchar('-');
		x = -x;
	}
	if (x >= 10)
		ft_putnbr(x / 10);
	ft_putchar((x % 10) + '0');
}
